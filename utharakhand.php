          <p>Pushpagiri (Kumara Parvatha), at 1,712 metres (5,617 ft), is the highest peak in Pushpagiri Wildlife Sanctuary in the Western Ghats of Karnataka. It is located in the Somwarpet Taluk, 20 kilometres from Somwarpet in the northern part of Kodagu district on the border between Dakshina Kannada and Kodagu district and hassan districts. It is 2nd highest peak of Karnataka.</p>
